package com.pillar.wordsearch;

import java.util.ArrayList;
import java.util.List;

public class Coordinate {

    private int x;
    private int y;

    public Coordinate (int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}